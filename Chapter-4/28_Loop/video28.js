console.log("For loop:");
for(let i = 0; i < 6; i++) {
    console.log("i =", i);
}

console.log("While loop:");
let count = 10;
while(count > 0) {
    console.log("Count =", count);
    count--;
}

console.log("Do-while loop:");
let count1 = -10;
do {
    console.log("Count1 =", count1);
    count1--;
} while(count1 > 0);

